package coreJava;

public class ConditionalOperators {

	public static void main(String[] args) {
		//Syntax ;  Condition ? Expression1 : Expression2;
		// In this if condition satisfied Expression1 executed if not satisfied Exp2 executed 
		int a = 5;
		int b = 10;
		int result = (a>b) ? a : b;
		System.out.println(result);//Conditional Operator

	}

}
